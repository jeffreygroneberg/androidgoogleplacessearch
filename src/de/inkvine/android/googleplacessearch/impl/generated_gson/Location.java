package de.inkvine.android.googleplacessearch.impl.generated_gson;

public class Location {
	private Number lat;
	private Number lng;

	public Number getLat() {
		return this.lat;
	}

	public void setLat(Number lat) {
		this.lat = lat;
	}

	public Number getLng() {
		return this.lng;
	}

	public void setLng(Number lng) {
		this.lng = lng;
	}
}
